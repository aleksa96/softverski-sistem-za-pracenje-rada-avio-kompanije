    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int row = tblPassengers.getSelectedRow();
        if (mtp.getListPassenger().isEmpty()) {
            JOptionPane.showMessageDialog(this, "There are no more passengers to select.");
            return;
        }

        if (row != -1) {
            Passenger p = mtp.getListPassenger().get(row);
            double price = Double.parseDouble(lblPrice.getText());
            User u = Controler.getInstance().getLoggedUser();
            Reservation r = new Reservation(-1, price, LocalDate.now(), u, fc, p, f);
            if (Controler.getInstance().getMtr().getListReservation().contains(r)) {
                JOptionPane.showMessageDialog(this, "Reservation is already added..");
                return;
            }
            mtr.addReservaion(r);
            mtp.removePassenger(p);
        } else {
            JOptionPane.showMessageDialog(this, "You have to select the passenger to add.");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int row = tblReservations.getSelectedRow();
        if (mtr.getListReservation().isEmpty()) {
            JOptionPane.showMessageDialog(this, "There are no more reservations to remove.");
            return;
        }
        if (row != -1) {
            Reservation r = mtr.getListReservation().get(row);
            mtp.addPassenger(r.getPassenger());
            mtr.removeReservaion(r);

        } else {
            JOptionPane.showMessageDialog(this, "You have to select the reservation to remove.");
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnMakeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMakeActionPerformed
        list = mtr.getListReservation();
        if (list.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No reservations to save.");
            return;
        }

        RequestGenerator.getInstance().sendRequest(new Request(Operation.SAVEGROUPRESERVATION, list));
    }//GEN-LAST:event_btnMakeActionPerformed

