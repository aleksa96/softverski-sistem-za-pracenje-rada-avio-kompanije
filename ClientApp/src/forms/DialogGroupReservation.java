    private void filter(String query) {
        TableModel tm = tblPassengers.getModel();
        TableRowSorter<TableModel> trs = new TableRowSorter<>(tm);

        tblPassengers.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(query));
    }

    private void prepareTablePassangers() {
        mtp = new ModelTablePassenger();
        Controler.getInstance().setMtpg(mtp);
        tblPassengers.setModel(mtp);
        refreshTable();
    }

    public void refreshTable() {
        RequestGenerator.getInstance().sendRequest(new Request(Operation.GETPASSANGERGROUP, null));
    }

    private void prepareTableReservations() {
        mtr = new ModelTableReservation();
        tblReservations.setModel(mtr);
    }

    void prepare(FlightClass fc, Flight f, double price) {
        this.price = price;
        this.f = f;
        this.fc = fc;

        lblFlight.setText(f + "");
        lblFlightClass.setText("" + fc);
        lblPrice.setText("" + price);
    }
