    private void prepareTable() {
        ModelTablePassenger mtp = new ModelTablePassenger();
        Controler.getInstance().setMtp(mtp);
        tblPassanger.setModel(mtp);
        refreshTable();
        //mtp.fireTableDataChanged();
    }

    public void refreshTable() {
        RequestGenerator.getInstance().sendRequest(new Request(Operation.GETPASSENGER, null));
    }
    
    private void filter(String query) {
        TableModel tm = tblPassanger.getModel();
        TableRowSorter<TableModel> trs = new TableRowSorter<>(tm);

        tblPassanger.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(query));
    }
