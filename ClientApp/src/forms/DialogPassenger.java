    private void btnChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeActionPerformed
        int row = tblPassanger.getSelectedRow();
        if (row != -1) {
            Passenger p = Controler.getInstance().getMtp().getListPassenger().get(row);
            Controler.getInstance().setPassengerToChange(p);

            dcp = new DialogChangePassenger(mf, true);
            Controler.getInstance().setDcp(dcp);
            dcp.pack();
            dcp.setLocationRelativeTo(null);
            dcp.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(this, "You need to select the passenger you want to change.");
        }
    }//GEN-LAST:event_btnChangeActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tblPassanger.getSelectedRow();
        if(row != -1) {
            Passenger p = Controler.getInstance().getMtp().getListPassenger().get(row);
            RequestGenerator.getInstance().sendRequest(new Request(Operation.DELETEPASSENGER, p));
        } else JOptionPane.showMessageDialog(this, "You need to select the passenger you want to delete.");
    }//GEN-LAST:event_btnDeleteActionPerformed

