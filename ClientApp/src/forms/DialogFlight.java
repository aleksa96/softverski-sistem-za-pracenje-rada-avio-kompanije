    private void prepareTable() {
        ModelTableFlight mtf = new ModelTableFlight();
        Controler.getInstance().setMtf(mtf);
        tblFlight.setModel(mtf);
        refreshTable();
    }
    
    private void filter(String query) {
        TableModel tm = tblFlight.getModel();
        TableRowSorter<TableModel> trs = new TableRowSorter<>(tm);

        tblFlight.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(query));
    }

    public void refreshTable() {
        RequestGenerator.getInstance().sendRequest(new Request(Operation.GETFLIGHT, null));
    }
