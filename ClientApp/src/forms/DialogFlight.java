    private void btnDeleteFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteFlightActionPerformed
        int row = tblFlight.getSelectedRow();
        if(row != -1) {
            Flight f = Controler.getInstance().getMtf().getListFlights().get(row);
            RequestGenerator.getInstance().sendRequest(new Request(Operation.DELETEFLIGHT, f));
        } else JOptionPane.showMessageDialog(this, "You need to select the flight you want to delete.");
    }//GEN-LAST:event_btnDeleteFlightActionPerformed

